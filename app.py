@app.callback(
    Output("material-content", "children"),
    Input({"type": "topic-link", "index": dash.ALL}, "n_clicks"),
    prevent_initial_call=True
)
def display_material(n_clicks):
    triggered = ctx.triggered_id
    if not triggered or all(c is None for c in n_clicks):
        return dash.no_update
    
    topic_name = triggered['index']
    content = materials_db.get(topic_name, "Материал в разработке.")
    
    return html.Div([
        dmc.Title(topic_name, order=4, mb="md", c="indigo"),
        dmc.Text(content, size="lg", style={"lineHeight": "1.6"}),
        html.Script("MathJax.Hub.Queue(['Typeset', MathJax.Hub]);")
    ])

